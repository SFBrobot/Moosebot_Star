typedef struct {
	bool val,
		valLast,
		delta,
		rise,
		fall;
} Toggle;

bool upTog(Toggle* tog, bool val) {
	tog->valLast = tog->val;
	tog->val = val;
	tog->delta = ((tog->val && !tog->valLast) || (!tog->val && tog->valLast)); //I don't like bad type warnings, so this is a manual xor
	tog->rise = tog->delta && tog->val;
	tog->fall = tog->delta && tog->valLast;
	return tog->val;
}

bool togVal(Toggle* tog) {
	return tog->val;
}

bool togValLast(Toggle* tog) {
	return tog->valLast;
}

bool togDelta(Toggle* tog) {
	return tog->delta;
}

bool togRising(Toggle* tog) {
	return tog->rise;
}

bool falling(Toggle* tog) {
	return tog->fall;
}



typedef struct {
	int target,
		val,
		valLast,
		err,
		mtrPwr;
	float prop,
		integ,
		integLim,
		deriv,
		kP,
		kI,
		kD;
} Pid;

void initPid(Pid* pid, float integLim, float kP, float kI, float kD) {
	pid->val =
		pid->valLast =
		pid->integ =
		pid->mtrPwr =
		0;
	pid->integLim = integLim;
	pid->kP = kP;
	pid->kI = kI;
	pid->kD = kD;
}

int setPidTarg(Pid* pid, int targ) {
	pid->target = targ;
	pid->integ = 0;
	return pid->target;
}

int upPid(Pid* pid, int val, int dt) {
	pid->val = val;
	pid->err = pid->target - pid->val; //Calculate error
	pid->prop = pid->err * pid->kP;  //Calculate proportional
	pid->integ += (pid->err * pid->kI) * dt; //Calculate integral
	if(fabs(pid->integ) > pid->integLim) //Collar integral
		pid->integ = pid->integLim * sgn(pid->integ);
	pid->deriv = pid->kD * (pid->val - pid->valLast) / dt; //Calculate derivative
	pid->mtrPwr = round(pid->prop + pid->integ + pid->deriv); //Calculate motor power
	return pid->mtrPwr;
}

int getPidPwr(Pid* pid) {
	return pid->mtrPwr;
}

task battLvls() {
	float battLvls[3] = { 0, 0, 0 };
	string battString[2];
	string* pBattString[2];

	for(int i = 0; i < 2; i++)
		pBattString[i] = &battString[i];

	for(int i = 0; i < 3; i++)
		battLvls[i] = 0;

	while(true) {

		for(int i = 0; i < 3; i++)
			battLvls[i] = 0;

		for(int i = 0; i < 10; i++) {
			battLvls[0] += nImmediateBatteryLevel / 10000.;
			battLvls[1] += SensorValue[in1] / 456.;
			battLvls[2] += BackupBatteryLevel / 10000.;
			wait1Msec(20);
		}

		sprintf(*pBattString[0], "%2.2fV || %2.2fV", battLvls[0], battLvls[1]);
		sprintf(*pBattString[1], "%2.2fV", battLvls[2]);

		displayLCDCenteredString(0, *pBattString[0]);
		displayLCDCenteredString(1, *pBattString[1]);

		wait1Msec(20);

	}
}

