task usercontrol() {
  int timeLast,
  	time,
  	dt,
  	sticks[4] = { 0, 0, 0, 0 };


	startTask(battLvls);

	while(true) {
		timeLast = time;
		time = nSysTime;
		dt = time - timeLast;
		upPid(&LiftPid, SensorValue[liftEnc], dt);

		upTog(&LiftTog, (vexRT[Btn5U] ^ vexRT[Btn5D]));

		for(int i = 0; i < 4; i++)
			sticks[i] = (fabs(vexRT[i]) > 7)
				? vexRT[i]
				: 0;

		setDrive(sticks[2] + sticks[0], sticks[2] - sticks[0]);

		if(falling(&LiftTog))
			setPidTarg(&LiftPid, SensorValue[liftEnc]);

		if(togVal(&LiftTog)) {
			if(vexRT[Btn5U])
				setLift(127);
			else
				setLift(-127);
		}
		
		else
			setLift(getPidPwr(&LiftPid));

		if(vexRT[Btn6U] ^ vexRT[Btn6D]) {
			if(vexRT[Btn6U])
				setClaw(true);
			else
				setClaw(false);
		}

		wait1Msec(20);

	}
}
